// Gets static page from next server and re-compiles it after one day
export const getStaticProps: GetStaticProps = async () => {
  // Retrieves product info from stripe
  const price = await stripe.prices.retrieve("price_1LCkesJxTLoGwi071FzzyB1S");

  const product = {
    priceId: price.id,
    amount: new Intl.NumberFormat("en-US", {
      style: "currency",
      currency: "USD",
    }).format(price.unit_amount / 100),
  };

  return {
    props: {
      product,
    },
    revalidate: 60 * 60 * 24, // 24 hours
  };
};
